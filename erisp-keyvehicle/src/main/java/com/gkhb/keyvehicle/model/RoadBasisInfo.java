package com.gkhb.keyvehicle.model;

import java.sql.Blob;


/**
*	道路基础信息表
*	@author weidongping
*	@createTime 2017年9月7日 上午10:27:02
*/
public class RoadBasisInfo {
	/**
	 * 主键id
	 */
	private String id;
	/**
	 * 道路名称
	 */
	private String roadName;
	/**
	 * 道路长度
	 */
	private double roadLength;
	/**
	 * 设计速度
	 */
	private double speedLimit;
	/**
	 * 道路区域
	 */
	private String roadRegion;
	/**
	 * 状态
	 */
	private int state;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRoadName() {
		return roadName;
	}
	public void setRoadName(String roadName) {
		this.roadName = roadName;
	}
	public double getRoadLength() {
		return roadLength;
	}
	public void setRoadLength(double roadLength) {
		this.roadLength = roadLength;
	}
	public double getSpeedLimit() {
		return speedLimit;
	}
	public void setSpeedLimit(double speedLimit) {
		this.speedLimit = speedLimit;
	}
	public String getRoadRegion() {
		return roadRegion;
	}
	public void setRoadRegion(String roadRegion) {
		this.roadRegion = roadRegion;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
	
	
}


