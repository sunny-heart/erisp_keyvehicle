package com.gkhb.keyvehicle.model;

import java.util.Date;

/**
 *	处置录入实体类
 *	@author chenxiaojie
 *	@createTime 2017年10月12日 下午5:15:19
 */
public class DisposalEntry {
	
	/**
	 * 主键id
	 */
	private String id;
	/**
	 * 预警信息id	
	 */
	private String warningId;
	/**
	 * 交管局处理部门
	 */
	private String jgEntryDepartment;
	/**
	 * 交管局录入人id
	 */
	private String jgEntryUserId;
	/**
	 * 交管局录入时间
	 */
	private Date  jgEntryTime;
	/**
	 * 交管局录入情况说明
	 */
	private String jgEntryInstructions;
	/**
	 * 行业主管处理部门
	 */
	private String hyEntryDepartment;
	/**
	 * 行业主管录入人id	
	 */
	private String  hyEntryUserId;
	/**
	 * 行业主管录入时间
	 */
	private Date hyEntryTime;
	/**
	 * 行业主管录入情况说明
	 */
	private String hyEntryInstructions;
	/**
	 * 状态
	 */
	private int state;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getWarningId() {
		return warningId;
	}
	public void setWarningId(String warningId) {
		this.warningId = warningId;
	}
	public String getJgEntryDepartment() {
		return jgEntryDepartment;
	}
	public void setJgEntryDepartment(String jgEntryDepartment) {
		this.jgEntryDepartment = jgEntryDepartment;
	}
	public String getJgEntryUserId() {
		return jgEntryUserId;
	}
	public void setJgEntryUserId(String jgEntryUserId) {
		this.jgEntryUserId = jgEntryUserId;
	}
	public Date getJgEntryTime() {
		return jgEntryTime;
	}
	public void setJgEntryTime(Date jgEntryTime) {
		this.jgEntryTime = jgEntryTime;
	}
	public String getJgEntryInstructions() {
		return jgEntryInstructions;
	}
	public void setJgEntryInstructions(String jgEntryInstructions) {
		this.jgEntryInstructions = jgEntryInstructions;
	}
	public String getHyEntryDepartment() {
		return hyEntryDepartment;
	}
	public void setHyEntryDepartment(String hyEntryDepartment) {
		this.hyEntryDepartment = hyEntryDepartment;
	}
	public String getHyEntryUserId() {
		return hyEntryUserId;
	}
	public void setHyEntryUserId(String hyEntryUserId) {
		this.hyEntryUserId = hyEntryUserId;
	}
	public Date getHyEntryTime() {
		return hyEntryTime;
	}
	public void setHyEntryTime(Date hyEntryTime) {
		this.hyEntryTime = hyEntryTime;
	}
	public String getHyEntryInstructions() {
		return hyEntryInstructions;
	}
	public void setHyEntryInstructions(String hyEntryInstructions) {
		this.hyEntryInstructions = hyEntryInstructions;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
	

}
