package com.gkhb.keyvehicle.model.export;


/*
 * 违法信息导出
 * 
 */
public class IllegalInfoExport {
	/**
	 * 车牌号码
	 */
	private String plateNumber;
	/**
	 * 所属企业
	 */
	private String ascriptionCompany;
	/**
	 * 企业联系人
	 */
	private String contacts;
	/**
	 * 联系电话
	 */
	private String phoneNumber;
	/**
	 * 主管部门
	 */
	private String competentAuthority;
	/**
	 * 违法类型
	 */
	private String illegalType;
	/**
	 * 违法时间
	 */
	private String illegalTime;
	/**
	 * 违法地点
	 */
	private String illegalPlace;
	/**
	 * 违法行为
	 */
	private String illegalBehavior;
	public String getPlateNumber() {
		return plateNumber;
	}
	public void setPlateNumber(String plateNumber) {
		this.plateNumber = plateNumber;
	}
	public String getAscriptionCompany() {
		return ascriptionCompany;
	}
	public void setAscriptionCompany(String ascriptionCompany) {
		this.ascriptionCompany = ascriptionCompany;
	}
	public String getContacts() {
		return contacts;
	}
	public void setContacts(String contacts) {
		this.contacts = contacts;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getCompetentAuthority() {
		return competentAuthority;
	}
	public void setCompetentAuthority(String competentAuthority) {
		this.competentAuthority = competentAuthority;
	}
	public String getIllegalType() {
		return illegalType;
	}
	public void setIllegalType(String illegalType) {
		this.illegalType = illegalType;
	}
	public String getIllegalTime() {
		return illegalTime;
	}
	public void setIllegalTime(String illegalTime) {
		this.illegalTime = illegalTime;
	}
	public String getIllegalPlace() {
		return illegalPlace;
	}
	public void setIllegalPlace(String illegalPlace) {
		this.illegalPlace = illegalPlace;
	}
	public String getIllegalBehavior() {
		return illegalBehavior;
	}
	public void setIllegalBehavior(String illegalBehavior) {
		this.illegalBehavior = illegalBehavior;
	}

}
